-- Captura de pantalla


-- Seleccionar un área
hl.bind(
    "SUPER + SHIFT + S",
    hl.dsp.exec_cmd("hyprshot -m region --clipboard-only")
)

-- Capturar una ventana
hl.bind(
    "SUPER + SHIFT + W",
    hl.dsp.exec_cmd("hyprshot -m window --clipboard-only")
)

-- Capturar un monitor
