-- Workspaces: 
-- SUPER + número: ir al workspace
-- SUPER + SHIFT + número: mover la ventana al workspace

for i = 1, 9 do
    hl.bind(
        "SUPER + " .. i,
        hl.dsp.focus({ workspace = i }),
        { description = "Ir al workspace " .. i }
    )

    hl.bind(
        "SUPER + SHIFT + " .. i,
        hl.dsp.window.move({
            workspace = i,
            follow = true,
        }),
        { description = "Mover ventana al workspace " .. i }
    )
end